	untuk menghindari risiko penyakit bertambah parah. 

    Penyakit asam lambung juga dikenal <br>dengan nama astroesophageal reflux 
    disease (GERD) yang membuat penderitanya merasa terbakar di dada akibat 
    asam <br>lambung naik ke kerongkongan. Gejala penyakit asam lambung umumnya 
    muncul minimal dua kali dalam seminggu.
    </p>
    
    <p>Penyakit ini bisa menyerang siapapun tanpa memandang usia atau jenis kelamin 
    dari anak-anak hingga orang dewasa. Gejala <br>penyakit ini sering diduga sebagai 
    serangan jantung atau penyakit jantung koroner, karena gejalanya yang hampir 
    mirip <br>dengan nyeri dada. Meskipun tidak mematikan seperti serangan jantung, 
    penyakit asam lambung perlu ditangani agar tidak <br>menimbulkan komplikasi.
    </p>

    <p>Bagi mereka yang mengalami asam lambung, akan mengalami gejala rasa seperti 
    terbakar di dada (heartburn), yang bertambah <br>parah setelah makan atau saat 
    berbaring. Gejala ini juga kerap disertai dengan keluhan gangguan pencernaan 
    lainnya, seperti <br>sering bersendawa, mual dan muntah, serta maag dan sesak napas. 

    Penyakit asam lambung juga dapat menimbulkan keluhan <br>mulut terasa asam.
    Adapun penyebab asam lambung menjadi naik karena proses otot kerongkongan 
    bagian bawah (otot LES) <br>melemah. Tak jarang, gejala asam lambung turut menjalar 
    hingga ke kerongkongan atau yang dikenal refluks asam lambung.
    </p>

    <p>Pengaruh otot LES bisa menyebabkan kontraksi dan menutup saluran ke 
    kerongkongan setelah makanan turun ke lambung. Bila <br>otot ini lemah, kerongkongan 
    akan tetap terbuka dan asam lambung naik kembali ke kerongkongan.

    Hal ini perlu diawasi secara <br>cermat, karena berisiko terjadi pada orang lanjut usia 
    (lansia), orang dengan obesitas, perokok, orang yang sering berbaring atau <br>tidur 
    setelah makan, serta wanita hamil.</p>

    <p>Ada sejumlah cara mengatasi asam lambung naik, salah satunya dengan 
    menggunakan antasida sebagai obat yang sering dicari. <br>Antasida dinilai ampuh 
    untuk meredakan asam lambung dengan cepat. Pada kasus asam lambung yang 
    lebih parah, pilihan obat <br>dengan resep dokter mungkin diperlukan untuk mencegah 
    kerusakan pada kerongkongan.</p>

    <p>Namun, antasida berpotensi menjadikan penderita asam lambung menjadi 
    kecanduan terhadap obat satu ini. Dilansir dari situs <br>informasi kesehatan 
    Halodoc.com, ada sejumlah cara untuk mengatasi asam lambung.</p>
	
	<p>Cara Menurunkan Asam Lambung yang Naik :</p>

    <h4>1. Jauhi makanan pemicu refluks</h4>

    <p>Cara mengatasi asam lambung naik adalah dengan menghindari makanan tertentu 
    yang memicu mulas, seperti kafein, soda, <br>cokelat, buah dan jus jeruk, tomat, bawang, 
    mint dan makanan berlemak tinggi.</p>

    <h4>2. Konsumsi makanan dengan kandungan banyak serat</h4>

    <p>Khasiat serat salah satunya adalah untuk membantu saluran pencernaan bekerja 
    dengan lancar dan sehat.</p>

    <h4>3. Mengatur jadwal porsi makan</h4>

    <p>Salah satu cara mengatasi asam lambung naik, yakni dengan menghindari makan 
    dalam porsi besar sekaligus. Cobalah makan lima <br>atau enam porsi kecil sehari.</p>

    <h4>4. Pantang makan sebelum tidur</h4>

    <p>Salah satu kebiasaan yang membuat asam lambung nudah naik adalah makan 
    sebelum tidur. Aktivitas kurang baik satu ini, sering <br>menjadi penyebab naiknya asam 
    lambung. Berhenti makan setidaknya dua atau tiga jam sebelum tidur, sehingga perut 
    memiliki <br>kesempatan untuk mengosongkannya sebelum Anda berbaring atau tidur.</p>

    <h4>5. Mengunyah makanan secara perlahan</h4>

    <p>Praktik cara mengatasi asam lambung naik satu ini terdengar ringan, namun sering 
    disepelekan. Padahal dampaknya sangat baik <br>untuk mencegah asam lambung naik.</p>

    <h4>6. Jauhi rokok</h4>

    <p>Kebiasaan merokok bisa mengurangi efektivitas otot yang menjaga asam di lambung. 
    Jadi, berhenti merokok bisa menjadi salah <br>satu cara mengatasi asam lambung naik 
    atau kambuh.</p>

    <h4>7. Kenakan pakaian longgar dan tidak ketat</h4>

    <p>Menggunakan pakaian yang longgar bisa menghindari Anda dari risiko mengalami 
    tekanan tambahan pada perut. Langkah ini <br>dijuga dinilai efektif sebagai cara mengatasi 
    asam lambung naik.</p>

    <h4>8. Mengatur poisis tidur</h4>

    <p>Pada saat tidur, Anda bisa menambahkan beberapa bantal di bagian kepala tempat tidur. 
    Tambahan bantal dilakukan agar posisi <br>kepala lebih tinggi sekitar enam inci. Langkah ini 
    dinilai cukup ampuh sebagai cara mengatasi asam lambung naik</p>

    <h4>9. Mengunyah permen karet</h4>

    <p>Aktivitas sepele ini juga dinilai mampu menjadi cara mengatasi asam lambung naik. 
    Dengan mengunyah permen karet, maka dapat <br>meningkatkan produktivitas air liur dalam 
    sistem pencernaan, sehingga memudahkan kinerja lambung saat memproses makanan 
    <br>yang masuk ke tubuh.</p>

    <p>Demikianlah cara mengatasi asam lambung tanpa harus meminum obat dan bisa 
    dilakukan di rumah dengan mudah. Selain itu <br>apabila penyakit asam lambung masih 
    belum menunjukkan adanya perubahan menuju sembuh, maka sangat dianjurkan untuk 
    <br>berkonsultasi dengan dokter.
	</p>
